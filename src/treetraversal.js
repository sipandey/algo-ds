/**
 * Tree Traversal
 * 1. Breadth First Search (works horizontally)
 * 2. Depth First Search
 *  - In-Order
 *  - Pre-Order
 *  - Post-Order
 *
 * BFS? DFS? Which is better?
 * If the tree is very wide, space complexity of BFS > DFS
 * BFS - Lot of nodes to keep track of!
 * DFS - Fewer nodes to keep track of!
 *
 * DFS InOrder - used commonly with BSTs
 * DFS PreOrder - can be used to "export" a tree structure
 * so that it is easily reconstructed or copied
 */